<p><strong>Seperti apa bentuk evaluasi dan kontrol di sekolah itu?</strong></p>
<p>Berikut ini adalah bentuk evaluasi dari masing-masing bidang pembelajaran:</p>
<h3 data-efek='tada'>Bidang Klasikal</h3>
<p>Bentuk soal diserahkan kepada guru. Artinya, guru dapat memberikan soal ujian semester dalam bentuk apapun, tidak mesti soal pilihan ganda dan esai.</p>
<p>Guru dapat memberikan soal yang tidak biasa, seperti ujian lisan maupun ujian praktek.</p>
<h3 data-efek='tada'>Bidang Hapalan Quran</h3>
<p>Untuk bidang hapalan Quran, maka bentuk evaluasinya adalah dengan mendudukkan siswa di hadapan gurunya lalu guru memberikan salah satu potongan ayat dan tugas siswa adalah melanjutkan potongan ayat tersebut. Lalu diulangi kembali, dan diulangi kembali.</p>
<p>Evaluasi ini dilakukan setiap akhir semester.</p>
<h3 data-efek='tada'>Bidang Ekskul</h3>
<p>Bentuk evaluasi dari bidang ekskul ini ada dua macam:</p>
<ol>
	<li>Evaluasi yang bisa dicek tiap hari</li>
	<li>Evaluasi yang dicek tiap akhir semester</li>
</ol>
<p>Adapun untuk evaluasi yang bisa dicek <strong>tiap hari</strong>, maka evaluasi itu menggunakan media <em>online</em> yaitu Instagram.</p>
<p>Di sekolah ini, setiap ekskul yang ada, wajib memiliki akun Instagram.</p>
<p>Nah, akun Instagram itu selain berguna sebagai media evaluasi pencapaian tiap ekskul, juga berfungsi sebagai media promosi bagi siswa supaya tertarik untuk mengikuti ekskul tersebut.</p>
<p>Sedangkan untuk evaluasi tiap <strong>akhir semester</strong>, maka dibentuklah suatu <em>event</em> yang memberikan <em>space</em> bagi tiap-tiap ekskul untuk menampilkan karya mereka sehingga bisa dinikmati bersama.</p>
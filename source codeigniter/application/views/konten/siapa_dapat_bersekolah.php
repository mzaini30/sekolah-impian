<p><strong>Siapa saja yang dapat bersekolah di sekolah ini?</strong></p>
<p>Sekolah ini ditujukan untuk jenjang SMP.</p>
<p data-efek='bounce'>Mengapa hanya jenjang SMP?</p>
<p>Tentu saja untuk mematangkan konsep sekolah ini. Ketika sudah dapat terimplementasi secara baik di tingkat SMP, barulah akan diolah dan disesuaikan untuk jenjang yang lain.</p>
<h1>Jumlah Siswa</h1>
<p>Jumlah siswa di setiap angkatannya sebanyak 40 siswa. Yang artinya, dalam satu sekolah terdapat 120 siswa.</p>
<p data-efek='rubberBand'>Mengapa hanya 40 siswa di setiap angkatannya?</p>
<p>Tentu saja untuk melakukan pengawasan yang lebih baik kepada setiap siswa. Jika setiap angkatannya lebih dari 40 siswa, maka akan lebih susah memantau setiap siswa.</p>
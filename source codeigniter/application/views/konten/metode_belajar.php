<p><strong>Bagaimana metode belajarnya?</strong></p>
<p>Untuk bagian klasikal, digunakan metode hanya mempelajari intinya.</p>
<p>Untuk bagian hapalan Quran, digunakan metode setoran hapalan Quran setiap pagi.</p>
<p>Untuk bagian ekskul, digunakan metode pendampingan untuk mengembangkan kelebihan.</p>
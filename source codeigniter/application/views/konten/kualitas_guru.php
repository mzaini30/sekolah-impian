<p><strong>Bagaimana kualitas guru yang mengajar nanti?</strong></p>
<p>Guru yang dimaksud dalam sekolah ini ada tiga hal:</p>
<h3>Guru Pelajaran Klasikal</h3>
<p>Klasifikasi guru yang sesuai dengan konsep sekolah ini:</p>
<ol>
	<li>Memiliki pengetahuan yang luas dengan bidang pelajaran yang diajarkannya</li>
	<li>Mampu mengenali potensi siswa</li>
	<li>Siap berpacu dengan kurikulum</li>
</ol>
<h3>Guru Hapalan Quran</h3>
<p>Klasifikasi guru hapalan Quran yang sesuai dengan konsep sekolah ini:</p>
<ol>
	<li>Hafidz 30 juz</li>
	<li>Mampu memotivasi siswa</li>
	<li>Memiliki kecintaan terhadap Quran</li>
</ol>
<h3>Guru Ekskul</h3>
<p>Klasifikasi guru ekskul yang sesuai dengan konsep sekolah ini:</p>
<ol>
	<li>Menguasai ekskul yang dibimbingnya</li>
	<li>Siap mempelajari berbagai hal baru</li>
	<li>Mampu bergaul dengan siswanya</li>
</ol>